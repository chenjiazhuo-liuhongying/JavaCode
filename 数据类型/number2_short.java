package study_itheima.数据类型;


public class number2_short {
    public static void main(String[] args) {
        //short类型:min：-32768（-2^15）,max:-32767(2^15-1)
        short number1 = 12345;
        short number2 = 22334;
        System.out.println("number1:"+number1);
        System.out.println("number2:"+number2);
    }
}
