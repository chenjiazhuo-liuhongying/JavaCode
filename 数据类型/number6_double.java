package study_itheima.数据类型;

public class number6_double {
    public static void main(String[] args) {
        //double类型是双精度，小数后可以加D或d，也可以不加
        double a = 2345.12345;
        double b = 12345.234567;
        System.out.println("a=:"+a);
        System.out.println("b=:"+b);
    }
}
